    private void jLabel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MouseClicked
        //Declaracion de Variables
        String Tipo ="";
        String Sabores = "";
        String Sabores2 = "";
        String Sabores3 = "";
        float Monto = 0;
        float tipo=0;
        
        // Tipo de Helado:
        if(jRadioButton_Crema.isSelected()){
            Tipo = "Crema";
            String Crema= "15";
            tipo = Float.parseFloat(Crema);
        }if(jRadioButton_Frutas.isSelected()){
            Tipo = "Fruta";
            String Fruta= "20";
            tipo = tipo + Float.parseFloat(Fruta);
        }if (jRadioButton_Laguna.isSelected()){
            Tipo="Laguna";
            String Laguna= "30";
            tipo = tipo + Float.parseFloat(Laguna);
        }if(jRadioButton_Xameleon.isSelected()){
            Tipo = "Xamaleon";
            String Xamaleon= "25";
            tipo = tipo + Float.parseFloat(Xamaleon);
        }
        
        //Sabores Especiales
        if(jCheckBox_Chocolate.isSelected()){
            Sabores = "Chocolate";
            String Chocolate= "1.5";
            Monto = Float.parseFloat(Chocolate);
        }if(jCheckBox_Limon.isSelected()){
            Sabores = Sabores + "   Limon";
            String Limon= "2.2";
            Monto = Monto + Float.parseFloat(Limon);
        }if(jCheckBox_Lucma.isSelected()){
            Sabores2 = Sabores2 + "   Lucma";
            String Lucma= "1.4";
            Monto = Monto + Float.parseFloat(Lucma);
        }if (jCheckBox_Mango.isSelected()){
            Sabores3 = Sabores3 + "   Mango";
            String Mango= "2.3";
            Monto = Monto + Float.parseFloat(Mango);
        }if (jCheckBox_Maracuya.isSelected()){
            Sabores3 = Sabores3 + " Maracuya";
            String Maracuya= "1.8";
            Monto = Monto + Float.parseFloat(Maracuya);
        }if (jCheckBox_Vainilla.isSelected()){
            Sabores2 = Sabores2 +" Vainilla";
            String Vainilla= "1.3";
            Monto = Monto + Float.parseFloat(Vainilla);
        }
        
        //Pantalla Tipo Helado
        jLabel_RespuestaTipo.setText(Tipo);
        //Pantalla Tipo Sabores
        jLabel_RespuestaSabores.setText(Sabores);
        jLabel_RespuestaSabores2.setText(Sabores2);
        jLabel_RespuestaSabores3.setText(Sabores3);
        //Pantalla Monto Pagar
        jLabel_RespuestaPagar.setText("S/ " + (Monto+tipo));
        
        
    }//GEN-LAST:event_jLabel10MouseClicked

