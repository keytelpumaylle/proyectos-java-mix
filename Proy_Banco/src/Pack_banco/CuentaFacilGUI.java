    private void cantidadTextFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cantidadTextFieldActionPerformed
        // CANTIDAD
        cuenta.setCantidad(Double.parseDouble(cantidadTextField.getText()));
        saldoLabel.setText("Saldo: $" + String.format("%.2f", cuenta.getCantidad()));
    }//GEN-LAST:event_cantidadTextFieldActionPerformed

    private void depositarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositarButtonActionPerformed
        // BOTON DEPOSITAR
        double cantidad = Double.parseDouble(cantidadTextField.getText());
        cuenta.depositar(cantidad);
        saldoLabel.setText("Saldo: $" + String.format("%.2f", cuenta.getCantidad()));
        mensajeTextArea.setText(cuenta.getTitular()+": Dep√≥sito realizado.");
    }//GEN-LAST:event_depositarButtonActionPerformed

    private void retirarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retirarButtonActionPerformed
        // BOTON RETIRAR
        double cantidad = Double.parseDouble(cantidadTextField.getText());
        saldoLabel.setText("Saldo: $" + String.format("%.2f", cuenta.getCantidad()));
        mensajeTextArea.setText(cuenta.retirar (cantidad));
 
    }//GEN-LAST:event_retirarButtonActionPerformed

